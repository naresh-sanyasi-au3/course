function sudoku(array){
  
    for(var i = 0; i < 9; i++) {
      for(var j = 0; j < 9; j++) {
        var position = array[i][j]
        if (position !== "."){
          for(var k = 0; k < 9; k++) {
            if(j !== k && array[i][k] === position)
              return false
          }
          for(var k = 0; k < 9; k++) {
            if(i !== k && array[j][k] === position) 
              return false
          }
          var a = i - i % 3
          var b = j - j % 3
          for (var x = a; x < a + 3; x++) {
            for (var y = b; y < b + 3; y++) {
              if (x !== i && y !== j && array[x][y] === position) 
                return false
            }
          }
        }
      }
      return true
    }
  }
  
  
